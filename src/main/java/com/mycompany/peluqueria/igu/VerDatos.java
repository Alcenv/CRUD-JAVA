    public void cargarTabla() {
        //Definir el modelo que tendra la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            //Fila y Columna no sean editables
            @Override
            public boolean isCellEditable (int row, int column){
                return false;
            }
        };
        
        //Nombres de las columnas (establecidos)
        String titulos[] = {"Num", "Nombre", "Color", "Raza", "Alérgico", "At.Especial", "Dueño", "Cel"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //Cargar datos desde la BD
        List <Mascota> listaMascotas = control.traerMascotas();
        
        //Recorre lista y muestra cada elemento en la tabla
        if (listaMascotas!=null){
            for (Mascota masco: listaMascotas){
                Object[] objeto ={masco.getNum_cliente(), masco.getNombre(), masco.getColor(), masco.getRaza(), masco.getAlergico(), masco.getAtencion_especial(), masco.getUnDuenio().getNombre(), masco.getUnDuenio().getCelDuenio()};
                modeloTabla.addRow(objeto);
            }
        }
        
        tablaMascotas.setModel(modeloTabla);
        
    }
